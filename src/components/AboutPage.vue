					describing men’s sexuality versus women’s reflects our culture of
					gender double standards and slut-shaming. We risk reinforcing and
					perpetuating these biases and negative values if we don’t interrogate
					the words we use and why. I think exploring language in terms of how
					genders are represented and interrogating how and why words are used
					is important for understanding and challenging sexism and patriarchy.
