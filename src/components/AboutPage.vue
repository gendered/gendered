				<h2 class="light">Notes</h2>
				<p>
					This project is <strong>not</strong> an invitation to "even" out the
					language and create or adopt more words that punch down. I do not
					believe in replacing male domination (exerting power over and at the
					expense of others) with female domination. As
					<a href="https://twitter.com/theafrolegalise" class="about-link">
						Nnennaya Amuchie
					</a>
					says, “I don't want to be equal to men. Men are not the standard of
					humanity. I want to live on my own terms free from violence.”
					Therefore it’s important to note that while the word “fuckboy” has
					taken on a new, empowering meaning through its use by femmes, it’s
					roots still lie in continual rape and abuse of
					<a
						href="https://www.theatlantic.com/politics/archive/2015/02/rape-in-the-american-prison/385550/"
						class="about-link"
					>
						feminized bodies
					</a>
					in the prison system. While words can be reclaimed and acquire
					positive associations, this reclamation is often done by the groups
					the word labels. There has been
					<a
						href="https://www.themarshallproject.org/2018/07/25/prison-rape-allegations-are-on-the-rise"
						class="about-link"
					>
						no such reckoning
					</a>
					in the prison system.
				</p>

				<p>
					While I hope this project can provide a space for deconstructing and
					examining gendered language, it does remain within and thus a part of
					the binary gender categorical system. It is a study of it, not an
					endorsement. The existence of gendered words like sissy and tomboy
					show that the terms female and male insufficiently categorize our
					experience and there is multiplicity within gender. Their often
					negative connotations that suggest the behaviour they describe is an
					unwelcome departure from “normal,” is a part of the system of labeling
					that I want to make data available to explore.
				</p>

				<p>
					Lastly this idea is not new. In the 1970s Julia Stanley scoured the
					Oxford English dictionary and found 220 terms referring to “women as
					prostitutes” compared to 20 for men. I’m maintaining a
					<a
						href="https://github.com/gendered/reading-list/blob/master/README.md"
						class="about-link"
					>
						reading list
					</a>
